


export function verifyVideoFile(fileType) {
    return (fileType === "video/mpeg" || fileType === 'AVI' || fileType === "video/mp4") ? true : false;
}

export function verifyFile(fileType) {
    return (fileType === "video/mpeg" || fileType === 'AVI' || fileType === "video/mp4") ? true : false;
}

